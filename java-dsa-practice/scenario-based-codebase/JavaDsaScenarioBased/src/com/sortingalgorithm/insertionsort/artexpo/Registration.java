package com.sortingalgorithm.insertionsort.artexpo;

public class Registration {

	private String name;
	private String time;
	
	Registration(String name, String time){
		this.name = name;
		this.time = time;
	}

	public String getName() {
		return name;
	}

	public String getTime() {
		return time;
	}
	
}
