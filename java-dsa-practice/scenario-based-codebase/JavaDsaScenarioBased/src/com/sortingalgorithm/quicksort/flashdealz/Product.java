package com.sortingalgorithm.quicksort.flashdealz;

public class Product {

	private String name;
	private int discount;
	
	Product(String name, int discount){
		this.name = name;
		this.discount = discount;
	}
	public String getName() {
		return name;
	}
	public int getDiscount() {
		return discount;
	}
	
}

